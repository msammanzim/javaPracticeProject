package Assignment_2;

public class Assignment_Operator {
    public static void main(String[] args) {
        int num1=25,num2=30;

        num1+=num2;
        System.out.println(num1);

        num1-=num2;
        System.out.println(num1);

        num1*=num2;
        System.out.println(num1);

        num1/=num2;
        System.out.println(num1);

    }
}
