package Assignment_2;

public class Relational_Operator {
    public static void main(String[] args) {
        int num1=50, num2=100;
        System.out.println(num1>num2);
        System.out.println(num1<num2);
        System.out.println(num1!=num2);
        System.out.println(num1>=num2);
        System.out.println(num1<=num2);
        System.out.println(num1==num2);





    }
}
