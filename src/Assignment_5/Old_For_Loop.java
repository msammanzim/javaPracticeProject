package Assignment_5;

public class Old_For_Loop {
    public static void main(String[] args) {
        int i,j;
        System.out.println("Incriment Number");
        for(i=1;i<=10;i++){
            System.out.println(i);
        }
        System.out.println("Decriment Number");
        for (j=10;j>=1;j--){
            System.out.println(j);
        }

    }
}
